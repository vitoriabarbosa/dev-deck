package devdeck.utils;

/**
 * A classe {@code ConfigCarta} contém configurações constantes relacionadas ao
 * tamanho e posicionamento das cartas no jogo. Essas constantes são usadas em
 * toda a aplicação para garantir consistência nas dimensões e no deslocamento
 * das cartas.
 */
public class ConfigCarta {

    /**
     * Largura padrão das cartas, em pixels.
     */
    public static final int LARGURA = 150;

    /**
     * Altura padrão das cartas, em pixels.
     */
    public static final int ALTURA = 200;

    /**
     * Deslocamento horizontal padrão entre as cartas, em pixels.
     */
    public static final int DESLOCAMENTO_X = 200;

    /**
     * Deslocamento vertical padrão entre as cartas, em pixels.
     */
    public static final int DESLOCAMENTO_Y = 40;
}
